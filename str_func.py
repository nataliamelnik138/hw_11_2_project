def converts_to_uppercase(value):
    """
    Переводит все буквы в верхних регистр
    :param value: исходная строка
    :return: строка, в которой все буквы в верхнем регистре
    """
    return value.upper()


def capitalizes_first_letter(value):
    """
    Переводит первую букву каждого слова в верхних регистр
    :param value: исходная строка
    :return: строка, в которой каждое слово начинается с заглавной буквы
    """
    return value.title()
